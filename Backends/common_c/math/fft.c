static uint32_t bitReverseUint32(uint32_t value, uint32_t log2N) {
	return ((uint32_t)(
		(bitReverseTable[ value        & 0xff] << 24) |
		(bitReverseTable[(value >> 8 ) & 0xff] << 16) |
		(bitReverseTable[(value >> 16) & 0xff] << 8 ) |
		(bitReverseTable[(value >> 24) & 0xff]      )
	) >> (32 - log2N));
}
